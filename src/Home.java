    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        String userid=jLabel1.getText();
        ResultSet rs = null;
        String text=jTextField1.getText();
        String brandtext=jTextField2.getText();
        String category=(String)jComboBox1.getSelectedItem();
        if(category.equals("All"))
            category=null;
        else category="'"+category+"'";
        
        String order="";
        if(jComboBox2.getSelectedIndex()==0)
            order="price";
        else if(jComboBox2.getSelectedIndex()==1)
            order="price desc";
        else if(jComboBox2.getSelectedIndex()==2)
            order="quantity";
        else if(jComboBox2.getSelectedIndex()==3)
            order="quantity desc";
        String lowprice="0",lowqnty="0",highprice="10000000",highqnty="100000000";
        if(jCheckBox1.isSelected())
        {
           lowprice=jTextField3.getText();
           highprice=jTextField4.getText();
        }
        if(jCheckBox2.isSelected())
        {
           lowqnty=jTextField5.getText();
           highqnty=jTextField6.getText();
        }
        
        
        
        String icode,iname,brand,qnty,threshold,price;
        try
        {   //System.out.println(order);
            model.setRowCount(0);
            Class.forName("java.sql.DriverManager");
            java.sql.Connection con=(java.sql.Connection)DriverManager.getConnection
            (EZStockerDataSource.connect,EZStockerDataSource.userid,EZStockerDataSource.passwd);
            java.sql.Statement stmt=(java.sql.Statement)con.createStatement();
            String query1="SELECT itemcode,name,category,brand,quantity,threshold,price FROM ITEM WHERE NAME LIKE '%"+text+"%' "
                    + " and userid='"+userid+"' and category = ifnull("+category+",category) and brand like '"+brandtext+"%'"
                    + " and price>="+lowprice+" and price<="+highprice+" and quantity<="+highqnty+" and"
                    + " quantity>="+lowqnty+" order by "+order+";";
            rs = stmt.executeQuery(query1);
            if(rs.next()==false)
            {JOptionPane.showMessageDialog(this,"No such product");
            jScrollPane1.setVisible(false);
            }
            else 
            {jScrollPane1.setVisible(true);
            rs.previous();}
            while (rs.next())
            {
                icode =rs.getString("ItemCode");
                iname=rs.getString("Name");
                brand = rs.getString("brand");
                category = rs.getString("category");
                qnty = rs.getString("quantity");
                threshold = rs.getString("threshold");
                price=rs.getString("price"); 
                model.addRow(new Object[] {icode,iname,category,brand,qnty,threshold,price});
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog (this, e.getMessage());
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

