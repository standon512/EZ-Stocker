    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String userid=jLabel1.getText();
        String StoredPasswd = "";
        String password = String.valueOf(jPasswordField1.getText());
        if (jPasswordField1.getPassword().length == 0)
        JOptionPane.showMessageDialog(this,"Current Password not Entered");
        else  {
            try {

                MessageDigest md = MessageDigest.getInstance("SHA-1");
                md.update(password.getBytes());
                byte byteData[] = md.digest();

                //convert the byte to hex format
                StringBuffer sb = new StringBuffer();
                for (int i = 0; i < byteData.length; i++)
                {
                    sb.append(Integer.toString((byteData[i] & 0xff) + 0x100, 16).substring(1));
                }

                //System.out.println("Hex format : " + sb.toString());
                Class.forName("java.sql.DriverManager");
                Connection con = (Connection)
                DriverManager.getConnection (EZStockerDataSource.connect,EZStockerDataSource.userid,EZStockerDataSource.passwd);
                Statement stmt = (Statement) con.createStatement();
                String query="SELECT passwd FROM user WHERE userid='"+userid+"';";
                ResultSet rs=stmt.executeQuery(query);
                if (rs.next())
                {
                    StoredPasswd  = rs.getString("passwd");
                    String hexpsswd=sb.toString();
                    if (hexpsswd.equals(StoredPasswd))
                    {
                        String newpass1 = String.valueOf(jPasswordField2.getText());
                        String newpass2 = String.valueOf(jPasswordField3.getText());
                        if(newpass1!=""&&newpass1.equals(newpass2))
                        {

                            MessageDigest md1 = MessageDigest.getInstance("SHA-1");
                            md1.update(newpass1.getBytes());
                            byte byteData1[] = md1.digest();
                            //convert the byte to hex format
                            StringBuffer sb1 = new StringBuffer();
                            for (int i = 0; i < byteData1.length; i++)
                            {
                                sb1.append(Integer.toString((byteData1[i] & 0xff) + 0x100, 16).substring(1));
                            }
                            String newhexpasswd=sb1.toString();
                            //System.out.println(newhexpasswd);
                            String query1="UPDATE USER SET PASSWD='"+newhexpasswd+"' where userid='"+userid+"';";
                            stmt.executeUpdate(query1);
                            JOptionPane.showMessageDialog(this, "Password updated successfully");
                        }
                        else JOptionPane.showMessageDialog(this, "Passwords Don't Match");
                    }
                }
                else JOptionPane.showMessageDialog(this, "Incorrect password");
            }
            catch (Exception e)
            {      JOptionPane.showMessageDialog(this, e.getMessage());}

        }
    }//GEN-LAST:event_jButton2ActionPerformed

